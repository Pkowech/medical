  async getAnalytics(userId: string): Promise<any> {
    try {
      const goals = await this.prisma.learningGoal.findMany({
        where: { userId },
        select: {
          id: true,
          title: true,
          status: true,
          category: true,
          priority: true,
          progress: true,
          targetDate: true,
          completedAt: true,
          createdAt: true,
          streakCount: true,
        },
      });

      const totalGoals = goals.length;
      const completedGoals = goals.filter(
        (g) => g.status === ProgressStatus.completed,
      ).length;
      const activeGoals = goals.filter(
        (g) => g.status === ProgressStatus.active,
      ).length;
      const overdue = goals.filter((g) => {
        if (g.status !== ProgressStatus.active) {
          return false;
        }
        if (!g.targetDate) {
          return false;
        }
        const td = g.targetDate as unknown as Date;
        return new Date(td) < new Date();
      }).length;

      // goals by priority
      const goalsByPriority = goals.reduce((acc: Record<string, number>, g) => {
        const p = String(g.priority ?? '0');
        acc[p] = (acc[p] || 0) + 1;
        return acc;
      }, {});

      // average completion time in days
      const completed = goals.filter(
        (g) =>
          g.status === ProgressStatus.completed && g.completedAt && g.createdAt,
      );
      const avgCompletionDays =
        completed.length > 0
          ? completed.reduce((sum, g) => {
              const created = g.createdAt as unknown as Date;
              const completedAt = g.completedAt as unknown as Date;
              return (
                sum +
                (completedAt.getTime() - created.getTime()) /
                  (1000 * 60 * 60 * 24)
              );
            }, 0) / completed.length
          : 0;

      // streaks
      const currentStreak = Math.max(
        0,
        ...goals.map((g) => g.streakCount || 0),
      );
      const longestStreak = currentStreak; // placeholder: we don't have historical data here
      const streakGoalIds = goals
        .filter((g) => (g.streakCount || 0) > 0)
        .map((g) => g.id);

      // upcoming deadlines
      const now = new Date();
      const upcomingDeadlines = goals
        .filter((g) => g.targetDate && new Date(g.targetDate) > now)
        .map((g) => ({
          goalId: g.id,
          title: g.title || '',
          targetDate: (g.targetDate as unknown as Date).toISOString(),
          daysRemaining: Math.ceil(
            ((g.targetDate as unknown as Date).getTime() - now.getTime()) /
              (1000 * 60 * 60 * 24),
          ),
        }));

      return {
        userId,
        totalGoals,
        completedGoals,
        activeGoals,
        overdueGoals: overdue,
        completionRate:
          totalGoals > 0 ? (completedGoals / totalGoals) * 100 : 0,
        goalsByCategory: this.groupGoalsByCategory(goals),
        goalsByPriority,
        averageCompletionTimeDays: Math.round(avgCompletionDays),
        streakData: {
          currentStreak,
          longestStreak,
        },
        streakGoalIds,
        upcomingDeadlines,
      };
    } catch (error) {
      this.logger.error('Error getting goal analytics:', error);
      throw new BadRequestException('Failed to get goal analytics');
    }
  }

  private groupGoalsByCategory(
    goals: Partial<LearningGoal>[],
  ): Record<string, number> {
    return goals.reduce((acc: Record<string, number>, goal) => {
      const category = goal.category || 'uncategorized';
      acc[category] = (acc[category] || 0) + 1;
      return acc;
    }, {});
  }

