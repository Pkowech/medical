  /**
   * GOAL-005: Cascade deadline extension to dependent goals
   */
  async cascadeDeadlineToDependent(
    goalId: string,
    newDeadline: Date,
    userId: string,
  ): Promise<void> {
    const dependentGoals = await (this.prisma as any).learningGoal.findMany({
      where: {
        userId,
        dependsOnGoalId: goalId,
      },
    });

    if (dependentGoals.length === 0) {
      return;
    }

    // Calculate days extended
    const parentGoal = await this.prisma.learningGoal.findUnique({
      where: { id: goalId },
    });

    if (!parentGoal?.targetDate) {
      return;
    }

    const daysExtended = Math.ceil(
      (newDeadline.getTime() - new Date(parentGoal.targetDate).getTime()) /
        (1000 * 60 * 60 * 24),
    );

    // Cascade to all dependents
    for (const dep of dependentGoals) {
      if (dep.targetDate) {
        const newDepDeadline = new Date(dep.targetDate);
        newDepDeadline.setDate(newDepDeadline.getDate() + daysExtended);

        await this.prisma.learningGoal.update({
          where: { id: dep.id },
          data: {
            targetDate: newDepDeadline,
            metadata: {
              ...((dep.metadata as Prisma.JsonObject) || {}),
              extensionReason: `Parent goal ${parentGoal.title} extended by ${daysExtended} days`,
            },
          },
        });

        this.logger.log(
          `Cascaded ${daysExtended} day extension from goal ${goalId} to dependent ${dep.id}`,
        );

        // Recursively cascade to children's children
        await this.cascadeDeadlineToDependent(dep.id, newDepDeadline, userId);
      }
    }
  }

  /**
   * GOAL-005: Mark dependent goals as blocked when parent fails
   */
  async blockDependentGoals(
    parentGoalId: string,
    userId: string,
    reason: string,
  ): Promise<void> {
    const dependentGoals = await (this.prisma as any).learningGoal.findMany({
      where: {
        userId,
        dependsOnGoalId: parentGoalId,
      },
    });

    for (const dep of dependentGoals) {
      await (this.prisma as any).learningGoal.update({
        where: { id: dep.id },
        data: {
          blockedReason: reason,
          status: ProgressStatus.dropped,
        },
      });

      await this.notificationsService.create(
        userId,
        `Goal "${dep.title}" is now blocked: ${reason}`,
        'warning',
        { goalId: dep.id, parentGoalId, type: 'goal_blocked' },
      );

      this.logger.warn(
        `Goal ${dep.id} blocked due to parent goal ${parentGoalId} failure`,
      );
    }
  }

  /**
   * GOAL-005: Validate no circular dependencies
   */
  async validateNoCycle(
    goalId: string,
    potentialParentId: string | null,
  ): Promise<boolean> {
    if (!potentialParentId) {
      return true; // No dependency, no cycle
    }

    if (goalId === potentialParentId) {
      return false; // Direct self-reference
    }

    // Traverse up the dependency chain
    let currentId: string | null = potentialParentId;
    const visited = new Set<string>([goalId]);

    while (currentId) {
      if (visited.has(currentId)) {
        return false; // Cycle detected
      }

      visited.add(currentId);

      const parentGoalResult: any = await (
        this.prisma as any
      ).learningGoal.findUnique({
        where: { id: currentId },
        select: { dependsOnGoalId: true },
      });

      currentId = parentGoalResult?.dependsOnGoalId ?? null;
    }

    return true; // No cycle
  }

