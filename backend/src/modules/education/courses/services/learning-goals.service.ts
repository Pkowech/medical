  private mapPriorityToNumber(priority: GoalPriority): number {
    switch (priority) {
      case 'high':
        return 3;
      case 'medium':
        return 2;
      case 'low':
        return 1;
      default:
        return 0;
    }
  }

  async create(
    createDto: CreateLearningGoalDto,
    userId: string,
  ): Promise<LearningGoal> {
    const user = await this.prisma.user.findUnique({ where: { id: userId } });
    if (!user) {
      throw new NotFoundException('User not found');
    }

    // GOAL-005: Validate dependency if provided
    if (createDto.dependsOnGoalId) {
      const isValid = await this.validateNoCycle(
        'new_goal', // Temporary ID for validation
        createDto.dependsOnGoalId,
      );
      if (!isValid) {
        throw new BadRequestException(
          'Circular dependency detected in goal setting.',
        );
      }
    }

    await this.validateRelatedResources(createDto);

    if (createDto.targetDate) {
      const conflicts = await this.detectConflicts(
        userId,
        createDto.targetDate,
      );
      if (conflicts.length > 0) {
        this.logger.warn(
          `Goal conflict detected for user ${userId}. ${conflicts.length} overlapping goals found.`,
        );
        // Note: In a real frontend integration, this would return a warning flag in the response
      }
    }

    const milestones: Milestone[] =
      createDto.milestones?.map((milestone, index) => ({
        ...milestone,
        id: milestone.id || `milestone_${Date.now()}_${index}`,
        completed: milestone.completed ?? false,
      })) || [];

    try {
      // Strip empty strings for optional FK fields so Prisma doesn't reject them
      const orNull = (v?: string | null) => (v && v.trim() !== '' ? v : undefined);

      const goal = await (this.prisma as any).learningGoal.create({
        data: {
          title: createDto.title,
          description: createDto.description,
          category: createDto.category,
          priority: createDto.priority
            ? this.mapPriorityToNumber(createDto.priority)
            : 1,
          startDate: createDto.startDate,
          targetDate: createDto.targetDate,
          userId,
          ...(orNull(createDto.courseId) ? { courseId: orNull(createDto.courseId) } : {}),
          ...(orNull(createDto.topicId) ? { topicId: orNull(createDto.topicId) } : {}),
          ...(orNull(createDto.materialId) ? { materialId: orNull(createDto.materialId) } : {}),
          ...(orNull(createDto.learningPathId) ? { learningPathId: orNull(createDto.learningPathId) } : {}),
          ...(orNull(createDto.dependsOnGoalId) ? { dependsOnGoalId: orNull(createDto.dependsOnGoalId) } : {}),
          streakCount: 0,
          status: ProgressStatus.inProgress,
          progress: 0,
          metadata: {
            milestones,
            targetCriteria: createDto.targetCriteria,
            progressTracking: createDto.progressTracking || {},
            reminderSettings: createDto.reminderSettings || {},
          } as any,
        },
        include: {
          user: true,
          course: true,
          LearningGoalProgress: true,
          dependsOn: true,
        },
      });

      await this.addProgressEntry(goal.id, userId, {
        entryType: ProgressEntryType.automatic,
        progressValue: 0,
        notes: 'Goal created',
        metadata: { autoGenerated: true },
      });

      this.eventEmitter.emit('learning-goal.created', {
        goalId: goal.id,
        userId,
        data: goal,
      });

      await this.notificationsService.create(
        userId,
        `New goal set: ${createDto.title}`,
        'info',
        { goalId: goal.id, type: 'goal_created' },
      );

      return goal;
    } catch (error) {
      throw new BadRequestException(
        `Failed to create learning goal: ${getErrorMessage(error)}`,
      );
    }
  }

  /**
   * Detects if a new goal conflicts with existing goals (deadlines within 7 days).
   */
  async detectConflicts(
    userId: string,
    targetDate: Date,
  ): Promise<LearningGoal[]> {
    const startWindow = new Date(targetDate);
    startWindow.setDate(startWindow.getDate() - 7);
    const endWindow = new Date(targetDate);
    endWindow.setDate(endWindow.getDate() + 7);

    return await this.prisma.learningGoal.findMany({
      where: {
        userId,
        status: { not: ProgressStatus.completed },
        targetDate: {
          gte: startWindow,
          lte: endWindow,
        },
      },
    });
  }

  async findAll(
    userId: string,
    filters: Partial<GoalFiltersDto> = {},
  ): Promise<LearningGoal[]> {
    const where: Prisma.LearningGoalWhereInput = { userId };

    // Handle status filter - "upcoming" is a special filter that checks targetDate
    if (filters.upcoming) {
      const now = new Date();
      where.targetDate = { gte: now };
      where.status = { not: ProgressStatus.completed };
    } else if (filters.status) {
      if (filters.status === 'active') {
        where.status = ProgressStatus.inProgress;
      } else {
        where.status = filters.status;
      }
    }

    if (filters.category) {
      where.category = filters.category;
    }
    if (filters.priority) {
      where.priority = this.mapPriorityToNumber(filters.priority);
    }

    if (filters.dueSoon) {
      const nextWeek = new Date();
      nextWeek.setDate(nextWeek.getDate() + 7);
      where.targetDate = { lte: nextWeek };
      where.status = { not: ProgressStatus.completed };
    }

    if (filters.overdue) {
      where.targetDate = { lt: new Date() };
      where.status = { not: ProgressStatus.completed };
    }

    if (filters.search) {
      where.OR = [
        { title: { contains: filters.search, mode: 'insensitive' } },
        { description: { contains: filters.search, mode: 'insensitive' } },
      ];
    }

    this.logger.debug(`FindAll filters: ${JSON.stringify(filters)}`);
    this.logger.debug(`Prisma where: ${JSON.stringify(where)}`);

    this.logger.debug(`FindAll filters: ${JSON.stringify(filters)}`);
    this.logger.debug(`Prisma where: ${JSON.stringify(where)}`);

    try {
      // Temporarily simplify query to isolate the "(not available)" column error
      const goals = await this.prisma.learningGoal.findMany({
        where,
        // include: {
        //   course: true,
        //   user: true,
        //   LearningGoalProgress: {
        //     orderBy: { createdAt: 'desc' },
        //     take: 5,
        //   },
        // },
        // orderBy: [{ priority: 'desc' }, { targetDate: 'asc' }],
      });
      return goals;
    } catch (error) {
      this.logger.error(`Error in findAll: ${getErrorMessage(error)}`, (error as any)?.stack);
      throw new BadRequestException(
        `Failed to retrieve goals: ${getErrorMessage(error)}`,
      );
    }
  }

