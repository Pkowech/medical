  private async validateRelatedResources(
    createDto: CreateLearningGoalDto,
  ): Promise<void> {
    if (createDto.courseId) {
      const course = await this.prisma.course.findUnique({
        where: { id: createDto.courseId },
      });
      if (!course) {
        throw new NotFoundException('Course not found');
      }
    }
  }

  private checkMilestoneAchievements(
    goal: LearningGoalWithMilestones,
    currentValue: number,
  ): void {
    const milestones = goal.milestones || [];
    if (!milestones.length) {
      return;
    }

    let milestonesChanged = false;

    for (const milestone of milestones) {
      if (!milestone.completed && currentValue >= milestone.targetValue) {
        milestone.completed = true;
        milestone.completedAt = new Date();
        milestonesChanged = true;

        this.eventEmitter.emit('learning-goal.milestone.achieved', {
          goalId: goal.id,
          milestoneId: milestone.id,
          userId: goal.userId,
          milestone,
        });

        void this.notificationsService.create(
          goal.userId,
          `Milestone achieved: ${milestone.title}`,
          'success',
          {
            goalId: goal.id,
            milestoneId: milestone.id,
            type: 'milestone_completed',
          },
        );
      }
    }

    if (milestonesChanged) {
      void this.prisma.learningGoal.update({
        where: { id: goal.id },
        data: {
          metadata: {
            ...((goal.metadata as Prisma.JsonObject) || {}),
            milestones,
          },
        },
      });
    }
  }

  private async updateStreak(goal: LearningGoal): Promise<void> {
    const today = new Date();
    const lastUpdate = goal.updatedAt;
    let streakCount = goal.streakCount || 0;

    if (!lastUpdate) {
      streakCount = 1;
    } else {
      const daysDiff = Math.floor(
        (today.getTime() - lastUpdate.getTime()) / (1000 * 60 * 60 * 24),
      );
      if (daysDiff === 1) {
        streakCount += 1;
      } else if (daysDiff > 1) {
        streakCount = 1;
      }
    }

    await this.prisma.learningGoal.update({
      where: { id: goal.id },
      data: { streakCount },
    });
  }

  private calculateCompletionPoints(goal: LearningGoalWithMilestones): number {
    let points = 100;

    switch (goal.priority) {
      case 3: // critical - was GoalPriority.critical
        points += 50;
        break;
      case 2: // high - was GoalPriority.high
        points += 30;
        break;
      case 1: // medium - was GoalPriority.medium
        points += 10;
        break;
      case 0: // low - was GoalPriority.low
        points += 5;
        break;
    }

    points += (goal.streakCount || 0) * 5;

    const milestoneCount = goal.milestones?.length || 0;
    points += milestoneCount * 10;

    return points;
  }

